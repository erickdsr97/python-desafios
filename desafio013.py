#algoritmo que irá mostrar o salário de um funcionário após receber 15% de aumento
antigo = int(input('Digite o seu salário: '))
aumento = antigo * 0.15
novo = antigo + aumento
print('O seu salário que era no valor de {}, passa a ser {}, após aumento de {}! Parabéns!'.format(antigo, novo, aumento))
