numero = int(input('Qual o seu número?: '))
dob = numero * 2
trip = numero * 3
raiz = int(numero**(1/2))
print('O seu número é {}, se multiplicarmos ele por dois o valor é {}, se multiplicarmos o seu número por 3 o valor é '
      '{}, e por fim a raiz quadrada de {} é {}'.format(numero, dob, trip, numero, raiz))

