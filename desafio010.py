# esse programa faz a conversão de quanto dinheiro uma pessoa tem para dólares
din = float(input('Quanto dinheiro Maria tem na carteira: '))
dol = 3.27
div = (din/dol)
print('O dinheiro que Maria possui é R${}, o valor do dólar é de R${} sendo assim ela pode comprar aproximadamente '
      'US${} dólares'.format(din, dol, div))
