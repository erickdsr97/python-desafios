# programa que faz a conversão de metros para centimetres e milímetros
metros = int(input('Digite o valor da metragem que deseja calcular: '))
cm = metros * 10
mm = metros * 100
print('O valor inserido é de {}m, em centímetros o valor fica {}cm e em milímetros o valor fica {}mm!'.format(metros, cm, mm))

